package Exercicio4_RafaelDamasceno;

public class PertenceAnimal {
	
	private Integer codcli;
	private Integer codAnimal;
	
	
	public PertenceAnimal(Integer codcli, Integer codAnimal) {
		this.codcli = codcli;
		this.codAnimal = codAnimal;
		
		
	}
	
	
	public Integer getCodcli() {
		return codcli;
	}
	public void setCodcli(Integer codcli) {
		this.codcli = codcli;
	}
	public Integer getCodAnimal() {
		return codAnimal;
	}
	public void setCodAnimal(Integer codAnimal) {
		this.codAnimal = codAnimal;
	}
	
	
	

}
